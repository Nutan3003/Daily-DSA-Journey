//Different ways to print the output statement in java
public class OutputStatement{
    public static void main(String[] args) {
        //Rule1:Println() method will display the ouput in new line.It will 1st print and then go to the next line
        System.out.println(100);
        //Output:100

        //Remember java is an Case sensative programming language.

        //Rule2:Print() will print the output in same line
        System.out.print(1000);
        System.out.print("Nutan");
        //Output:1000Nutan

        System.out.println("Nutan");
        System.out.print("Kate");
        System.out.println(22468);
        //Output:NutanKate
        //22468
        
    }
}